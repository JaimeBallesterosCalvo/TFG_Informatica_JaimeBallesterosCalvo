def guardar_anotaciones_segmentacion(results, frame_num, video_nombre, carpeta_anotaciones, frame_width, frame_height):
    """Guarda las anotaciones de SEGMENTACIÓN (polígonos) en archivos de texto"""

    # Crear nombre de archivo para este frame
    nombre_archivo = f"{video_nombre}_frame_{frame_num:06d}.txt"
    ruta_anotacion = os.path.join(carpeta_anotaciones, nombre_archivo)

    with open(ruta_anotacion, 'w') as f:
        # Verificar si hay detecciones
        if results[0].masks is not None and len(results[0].masks) > 0:
            # Obtener todas las detecciones de segmentación
            boxes = results[0].boxes
            masks = results[0].masks

            # Convertir a numpy de forma segura
            def safe_to_numpy(tensor_or_array):
                if hasattr(tensor_or_array, 'cpu'):  # Es un tensor de PyTorch
                    return tensor_or_array.cpu().numpy()
                else:  # Ya es un array de numpy
                    return tensor_or_array

            classes = safe_to_numpy(boxes.cls)
            confidences = safe_to_numpy(boxes.conf)

            # Escribir cada detección de segmentación
            for i in range(len(classes)):
                class_id = int(classes[i])
                class_name = results[0].names[class_id]
                confidence = confidences[i]

                # Obtener el polígono de segmentación
                if masks[i] is not None:
                    try:
                        # Obtener coordenadas del polígono - manejar diferentes formatos
                        if hasattr(masks[i].xy[0], 'cpu'):
                            polygon = masks[i].xy[0].cpu().numpy()  # Forma: (n, 2)
                        else:
                            polygon = masks[i].xy[0]  # Ya es numpy array

                        # Convertir a formato normalizado (0-1)
                        polygon_normalized = polygon / np.array([frame_width, frame_height])

                        # Aplanar el polígono para guardarlo
                        polygon_flat = polygon_normalized.reshape(-1)

                        # Escribir en formato: clase confianza x1 y1 x2 y2 ... xn yn
                        linea = f"{class_id} {confidence:.4f} " + " ".join([f"{coord:.6f}" for coord in polygon_flat]) + "\n"
                        f.write(linea)

                    except Exception as e:
                        print(f"⚠️  Error procesando máscara {i}: {e}")
                        # Fallback a bounding box si hay error con la máscara
                        if boxes.xyxy is not None:
                            bbox = safe_to_numpy(boxes.xyxy[i])
                            x1, y1, x2, y2 = bbox
                            # Convertir bbox a formato de polígono (4 puntos)
                            bbox_polygon = np.array([[x1, y1], [x2, y1], [x2, y2], [x1, y2]])
                            bbox_normalized = bbox_polygon / np.array([frame_width, frame_height])
                            bbox_flat = bbox_normalized.reshape(-1)
                            linea = f"{class_id} {confidence:.4f} " + " ".join([f"{coord:.6f}" for coord in bbox_flat]) + "\n"
                            f.write(linea)
                else:
                    # Si no hay máscara, usar bounding box como fallback
                    if boxes.xyxy is not None:
                        bbox = safe_to_numpy(boxes.xyxy[i])
                        x1, y1, x2, y2 = bbox
                        # Convertir bbox a formato de polígono (4 puntos)
                        bbox_polygon = np.array([[x1, y1], [x2, y1], [x2, y2], [x1, y2]])
                        bbox_normalized = bbox_polygon / np.array([frame_width, frame_height])
                        bbox_flat = bbox_normalized.reshape(-1)
                        linea = f"{class_id} {confidence:.4f} " + " ".join([f"{coord:.6f}" for coord in bbox_flat]) + "\n"
                        f.write(linea)
        else:
            # No hay detecciones en este frame
            f.write("# Sin detecciones en este frame\n")

    return ruta_anotacion

