def encontrar_carpetas_split(ruta_base, split):
    """Encuentra carpetas de imágenes y etiquetas para un split específico"""
    posibles_images = []
    posibles_labels = []

    # Estrategia 1: Estructura estándar YOLO
    std_images = os.path.join(ruta_base, split, 'images')
    std_labels = os.path.join(ruta_base, split, 'labels')

    if os.path.exists(std_images) and os.listdir(std_images):
        posibles_images.append(std_images)
    if os.path.exists(std_labels) and os.listdir(std_labels):
        posibles_labels.append(std_labels)

    # Estrategia 2: Búsqueda recursiva
    for root, dirs, files in os.walk(ruta_base):
        # Solo considerar si estamos en el nivel del split
        if split.lower() in root.lower():
            # Buscar carpeta de imágenes
            if 'images' in root.lower() and any(f.lower().endswith(('.jpg', '.jpeg', '.png')) for f in files):
                if root not in posibles_images:
                    posibles_images.append(root)

            # Buscar carpeta de etiquetas
            if 'labels' in root.lower() and any(f.endswith('.txt') for f in files):
                if root not in posibles_labels:
                    posibles_labels.append(root)

    # Priorizar rutas que tengan "images" en el nombre
    images_prioritarias = [r for r in posibles_images if 'images' in r.lower()]
    labels_prioritarias = [r for r in posibles_labels if 'labels' in r.lower()]

    # Si hay rutas prioritarias, usarlas; si no, usar las primeras encontradas
    origen_images = images_prioritarias[0] if images_prioritarias else (posibles_images[0] if posibles_images else None)
    origen_labels = labels_prioritarias[0] if labels_prioritarias else (posibles_labels[0] if posibles_labels else None)

    return origen_images, origen_labels

