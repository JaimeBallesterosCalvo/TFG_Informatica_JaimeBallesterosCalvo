def encontrar_y_leer_data_yaml(directorio):
    """Busca recursivamente el archivo data.yaml y lo lee"""
    for root, dirs, files in os.walk(directorio):
        if 'data.yaml' in files:
            data_yaml_path = os.path.join(root, 'data.yaml')
            try:
                with open(data_yaml_path, 'r') as f:
                    data_yaml = yaml.safe_load(f)
                return data_yaml, data_yaml_path
            except Exception as e:
                print(f"      ❌ Error leyendo data.yaml: {e}")
                return None, None
    return None, None

