def procesar_dataset_corregido(dataset_info, contador_global):
    """Versión corregida para copiar imágenes y etiquetas"""
    nombre = dataset_info['nombre']
    ruta = dataset_info['ruta']
    clases_originales = dataset_info['clases']

    print(f"\n🔄 PROCESANDO: {nombre}")
    print(f"   📁 Ruta del dataset: {ruta}")

    # Crear mapeo específico para este dataset
    mapeo_dataset = {}
    for idx_original, clase_original in enumerate(clases_originales):
        mapeo_dataset[idx_original] = mapeo_maestro[clase_original]

    print(f"   🔄 Mapeo: {mapeo_dataset}")

    total_imagenes_procesadas = 0
    total_etiquetas_procesadas = 0

    for split in ['train', 'valid', 'test']:
        print(f"\n   🔍 BUSCANDO SPLIT: {split}")

        origen_images, origen_labels = encontrar_carpetas_split(ruta, split)

        print(f"      📂 Ruta imágenes: {origen_images}")
        print(f"      📂 Ruta etiquetas: {origen_labels}")

        if not origen_images:
            print(f"      ⚠️  No se encontraron imágenes para {split}")
            continue

        destino_images = os.path.join(UNIFIED_DATASET_DIR, split, 'images')
        destino_labels = os.path.join(UNIFIED_DATASET_DIR, split, 'labels')

        # COPIAR IMÁGENES
        try:
            if origen_images and os.path.exists(origen_images):
                imagenes = [f for f in os.listdir(origen_images) if f.lower().endswith(('.jpg', '.jpeg', '.png'))]
                print(f"      🖼️  Encontradas {len(imagenes)} imágenes")

                for archivo in imagenes:
                    nuevo_nombre = f"ds{contador_global:02d}_{split}_{archivo}"
                    shutil.copy2(
                        os.path.join(origen_images, archivo),
                        os.path.join(destino_images, nuevo_nombre)
                    )
                    total_imagenes_procesadas += 1

                print(f"      ✅ Copiadas {len(imagenes)} imágenes")
            else:
                print(f"      ❌ No se pudo acceder a la carpeta de imágenes: {origen_images}")

        except Exception as e:
            print(f"      ❌ Error copiando imágenes: {e}")

        # COPIAR Y CONVERTIR ETIQUETAS
        if origen_labels and os.path.exists(origen_labels):
            try:
                etiquetas = [f for f in os.listdir(origen_labels) if f.endswith('.txt')]
                print(f"      📝 Encontradas {len(etiquetas)} etiquetas")

                for archivo in etiquetas:
                    nuevo_nombre = f"ds{contador_global:02d}_{split}_{archivo}"
                    origen_etiqueta = os.path.join(origen_labels, archivo)
                    destino_etiqueta = os.path.join(destino_labels, nuevo_nombre)

                    try:
                        with open(origen_etiqueta, 'r') as f:
                            lineas_originales = f.readlines()

                        lineas_convertidas = []
                        for linea in lineas_originales:
                            partes = linea.strip().split()
                            if len(partes) >= 5:
                                clase_original_idx = int(partes[0])
                                clase_maestra_idx = mapeo_dataset[clase_original_idx]
                                partes[0] = str(clase_maestra_idx)
                                lineas_convertidas.append(' '.join(partes) + '\n')

                        with open(destino_etiqueta, 'w') as f:
                            f.writelines(lineas_convertidas)

                        total_etiquetas_procesadas += 1

                    except Exception as e:
                        print(f"      ❌ Error procesando etiqueta {archivo}: {e}")

                print(f"      ✅ Procesadas {len(etiquetas)} etiquetas")

            except Exception as e:
                print(f"      ❌ Error general con etiquetas: {e}")
        else:
            print(f"      ⚠️  No se encontraron etiquetas para {split}")

    print(f"   ✅ {nombre} procesado: {total_imagenes_procesadas} imágenes, {total_etiquetas_procesadas} etiquetas")
    return total_imagenes_procesadas, total_etiquetas_procesadas

