OUTPUT_FOLDER = f"/content/drive/MyDrive/frames_{VIDEO_NAME.split('.')[0]}"

# Parámetros de extracción
FRAME_INTERVAL = 10  # 1 de cada 10 frames
MAX_FRAMES = 150     # Máximo por si acaso

print("🚀 CONFIGURANDO EXTRACCIÓN...")
print(f"Video: {VIDEO_NAME}")
print(f"Salida: {OUTPUT_FOLDER}")
print(f"Intervalo: cada {FRAME_INTERVAL} frames")
print(f"Máximo: {MAX_FRAMES} frames")

# Crear carpeta de salida
os.makedirs(OUTPUT_FOLDER, exist_ok=True)
print(f"✅ Carpeta creada: {OUTPUT_FOLDER}")

# Iniciar extracción
cap = cv2.VideoCapture(VIDEO_PATH)
frame_count = 0
saved_count = 0

print("\n🔄 EXTRAYENDO FRAMES...")

while True:
    ret, frame = cap.read()

    if not ret:
        break

    # Extraer cada 10 frames
    if frame_count % FRAME_INTERVAL == 0:
        # Guardar frame
        filename = f"frame_{saved_count:04d}.jpg"
        output_path = os.path.join(OUTPUT_FOLDER, filename)
        cv2.imwrite(output_path, frame)
        saved_count += 1

        # Mostrar progreso cada 10 frames guardados
        if saved_count % 10 == 0:
            print(f"   📸 {saved_count} frames guardados...")

    frame_count += 1

    # Detener si alcanzamos el máximo
    if saved_count >= MAX_FRAMES:
        break

cap.release()

