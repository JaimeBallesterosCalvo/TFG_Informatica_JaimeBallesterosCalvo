def process_video(input_path, output_path, conf=0.5):
    """
    Procesa un video con el modelo de segmentación YOLOv8
    """
    print(f"🎥 PROCESANDO VIDEO: {input_path}")

    # Verificar que el video existe
    if not os.path.exists(input_path):
        print(f"❌ ERROR: El video no existe en {input_path}")
        return None

    # Extraer información del video de entrada
    video_name = os.path.splitext(os.path.basename(input_path))[0]

    # YOLO guarda los resultados en una carpeta 'predict' por defecto
    # Vamos a controlar explícitamente dónde se guarda
    try:
        # Realizar predicción en el video
        results = best_model.predict(
            source=input_path,
            conf=conf,
            save=True,  # Esto hace que YOLO guarde el video
            project=os.path.dirname(output_path),
            name=video_name + "_results",
            exist_ok=True  # Sobrescribir si existe
        )

        # BUSCAR DÓNDE GUARDÓ YOLO EL VIDEO
        print("🔍 BUSCANDO EL VIDEO PROCESADO...")

        # Rutas donde YOLO podría haber guardado el video
        possible_paths = [
            os.path.join(os.path.dirname(output_path), video_name + "_results", video_name + ".mp4"),
            os.path.join(os.path.dirname(output_path), video_name + "_results", "video.mp4"),
            os.path.join(os.path.dirname(output_path), video_name + "_results", os.path.basename(input_path)),
            f"/content/runs/segment/{video_name}_results/{os.path.basename(input_path)}",
            f"/content/runs/segment/predict/{os.path.basename(input_path)}",
            f"/content/runs/segment/predict/video.mp4"
        ]

        found_video = None
        for path in possible_paths:
            if os.path.exists(path):
                found_video = path
                print(f"✅ VIDEO ENCONTRADO EN: {path}")
                break

        if found_video:
            # Copiar el video a la ubicación deseada
            shutil.copy(found_video, output_path)
            print(f"✅ VIDEO COPIADO A: {output_path}")

            # Mostrar información del video procesado
            cap = cv2.VideoCapture(output_path)
            fps = cap.get(cv2.CAP_PROP_FPS)
            frame_count = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
            cap.release()

            print(f"📊 INFORMACIÓN DEL VIDEO:")
            print(f"   - FPS: {fps}")
            print(f"   - Frames: {frame_count}")
            print(f"   - Duración: {frame_count/fps:.2f} segundos")

            return results
        else:
            print("❌ NO SE PUDO ENCONTRAR EL VIDEO PROCESADO")
            print("🔍 BUSCANDO EN TODAS LAS CARPETAS...")

            # Buscar recursivamente
            for root, dirs, files in os.walk('/content/'):
                for file in files:
                    if file.endswith('.mp4') and video_name in file:
                        found_path = os.path.join(root, file)
                        print(f"🎥 POSIBLE VIDEO: {found_path}")

            return None

    except Exception as e:
        print(f"❌ ERROR durante el procesamiento: {e}")
        return None

