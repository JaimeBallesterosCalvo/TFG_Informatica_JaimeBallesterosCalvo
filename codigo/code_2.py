    def parsear_anotacion_simple(self, archivo_anotacion, ancho_frame, alto_frame):
        detecciones = []
        elementos_ignorar = ['player-team1', 'player-team2', 'goalkeeper-team1', 'goalkeeper-team2', 'referee', 'ball', 'outsider']

        try:
            with open(archivo_anotacion, 'r') as f:
                lineas = f.readlines()

            for linea in lineas:
                linea = linea.strip()
                if not linea or linea.startswith('#'):
                    continue

                partes = linea.split()
                if len(partes) < 3:
                    continue

                clase_nombre = partes[0]
                if clase_nombre in elementos_ignorar:
                    continue

                coordenadas = partes[2:]
                puntos_norm = []

                for i in range(0, len(coordenadas)):
                    try:
                        punto = float(coordenadas[i])
                        puntos_norm.append(punto)
                    except ValueError:
                        continue

                if len(puntos_norm) % 2 != 0:
                    puntos_norm = puntos_norm[:-1]

                if len(puntos_norm) < 4:
                    continue

                puntos_absolutos = []
                for i in range(0, len(puntos_norm), 2):
                    x = int(puntos_norm[i] * ancho_frame)
                    y = int(puntos_norm[i + 1] * alto_frame)
                    puntos_absolutos.append([x, y])

                if len(puntos_absolutos) < 2:
                    continue

                try:
                    puntos_array = np.array(puntos_absolutos, dtype=np.float32)
                    area = cv2.contourArea(puntos_array)
                    if area <= 0:
                        continue

                    centroide = self.calcular_centroide_poligono(puntos_absolutos)

                    detecciones.append({
                        'clase_nombre': clase_nombre,
                        'centroide': centroide,
                        'area': area,
                        'porcentaje_frame': (area / (ancho_frame * alto_frame)) * 100
                    })

                except Exception:
                    continue

        except Exception as e:
            print(f"⚠️  Error leyendo {archivo_anotacion.name}: {e}")

        return detecciones

