        court_mas_reciente = self.historial_courts[-1]

        courts_similares = 0
        for court in self.historial_courts:
            if self.son_courts_similares(court, court_mas_reciente):
                courts_similares += 1

        umbral_consenso = len(self.historial_courts) * 0.6

        if courts_similares >= umbral_consenso:
            print(f"   ✅ Consenso alcanzado: {courts_similares}/{len(self.historial_courts)} courts similares")
            return court_mas_reciente
        else:
            print(f"   ⚠️  Consenso bajo: {courts_similares}/{len(self.historial_courts)} courts similares")
            return court_mas_reciente

