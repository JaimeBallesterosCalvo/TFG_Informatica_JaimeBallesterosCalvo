class CalibradorOptimo:
    def __init__(self, gestor):
        self.g = gestor

    def simular_frame(self, detecciones, cx_cam, cy_cam, fov_rad):
        if not detecciones: return set()
        angulos, pesos = [], []
        tan_fov_half = math.tan(fov_rad / 2)

        for det in detecciones:
            if det['clase'] not in self.g.centros_objetos: continue
            obj_x, obj_y = self.g.centros_objetos[det['clase']]
            dx = obj_x - cx_cam
            dy = obj_y - cy_cam
            angulo_obj_mapa = math.atan2(dy, dx)
            offset_screen = det['x'] - 0.5
            angulo_correccion = math.atan(2 * offset_screen * tan_fov_half)
            angulo_cam = angulo_obj_mapa - angulo_correccion
            p = 1.0 - abs(offset_screen)
            if 'area' in det['clase']: p *= 3.0
            else: p *= 0.5
            angulos.append(angulo_cam)
            pesos.append(p)

        if not angulos: return set()
        sin_sum = sum(w * math.sin(a) for w, a in zip(pesos, angulos))
        cos_sum = sum(w * math.cos(a) for w, a in zip(pesos, angulos))
        angulo_final = math.atan2(sin_sum, cos_sum)

        visibles = set()
        fov_half = fov_rad / 2
        for nombre, (ox, oy) in self.g.centros_objetos.items():
            dx = ox - cx_cam
            dy = oy - cy_cam
            a_obj = math.atan2(dy, dx)
            diff = a_obj - angulo_final
            while diff <= -math.pi: diff += 2*math.pi
            while diff > math.pi: diff -= 2*math.pi
            if abs(diff) < (fov_half * 1.1):
                visibles.add(nombre)
        return visibles

    def buscar_mejor_configuracion(self, archivos, max_frames=50):
        print("\n🔧 Auto-Calibrando...")
        datos_test = []
        for f in archivos[:max_frames]:
            dets = self.g.leer_anotacion(f)
            if dets:
                static_dets = [d for d in dets if d['clase'] in self.g.centros_objetos]
                if static_dets:
                    real_set = set(d['clase'] for d in static_dets)
                    datos_test.append((static_dets, real_set))

        if not datos_test: return None

        centro_x = self.g.w_tablero // 2
        posibles_x = [300, 400, 500, centro_x, centro_x + 300]
        posibles_y = [self.g.h_tablero + 200, self.g.h_tablero + 400, self.g.h_tablero + 600]
        posibles_fov = [30, 35, 42, 50, 60]

        mejor_score = float('inf')
        mejor_config = None

        for px in posibles_x:
            for py in posibles_y:
                for fov_deg in posibles_fov:
                    fov_rad = math.radians(fov_deg)
                    errores = 0
                    for detecciones, real_set in datos_test:
                        teorico_set = self.simular_frame(detecciones, px, py, fov_rad)
                        extra = len(teorico_set - real_set)
                        falta = len(real_set - teorico_set)
                        errores += (extra * 1.0) + (falta * 2.0)
                    if errores < mejor_score:
                        mejor_score = errores
                        mejor_config = (px, py, fov_deg)

        print(f"🎯 CONFIG: X:{mejor_config[0]} | Y:{mejor_config[1]} | FOV:{mejor_config[2]}°")
        return mejor_config

