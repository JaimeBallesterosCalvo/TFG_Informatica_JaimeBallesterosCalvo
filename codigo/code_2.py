    def leer_anotacion(self, path):
        detecciones = []
        try:
            with open(path, 'r') as f:
                for linea in f:
                    partes = linea.split()
                    if len(partes) < 3: continue
                    clase = partes[0]
                    coords_raw = [float(x) for x in partes[2:]]
                    puntos = []
                    for i in range(0, len(coords_raw), 2):
                        puntos.append([coords_raw[i], coords_raw[i+1]])
                    puntos_np = np.array(puntos, dtype=np.float32)
                    cx = np.mean(puntos_np[:, 0])
                    cy = np.mean(puntos_np[:, 1])
                    feet_y = np.max(puntos_np[:, 1])
                    detecciones.append({'clase': clase, 'x': cx, 'pies_norm': (cx, feet_y), 'poly_points': puntos_np})
        except: pass
        return detecciones

