    def cargar_recursos(self):
        self.path_tablero = "/content/drive/MyDrive/Recursos_Necesarios/Tablero.jpg"
        self.path_json = "/content/drive/MyDrive/Recursos_Necesarios/estructura_dual.json"

        if not os.path.exists(self.path_tablero): raise Exception("Falta Tablero")
        self.tablero = cv2.imread(self.path_tablero)
        self.h_tablero, self.w_tablero = self.tablero.shape[:2]

        with open(self.path_json, 'r') as f:
            self.geo_data = json.load(f)

        self.centros_objetos = {}
        self.contours_objetos = {}

        self.precalcular_objetos()

    def precalcular_objetos(self):
        def procesar(nombre, datos):
            if not datos: return
            pts_norm = datos.get('puntos_normalizados', [])
            if not pts_norm: return
            pts_pix = []
            for i in range(0, len(pts_norm), 2):
                pts_pix.append([int(pts_norm[i]*self.w_tablero), int(pts_norm[i+1]*self.h_tablero)])
            pts_np = np.array(pts_pix, dtype=np.int32)

            # Centroide
            M = cv2.moments(pts_np)
            if M["m00"] != 0:
                self.centros_objetos[nombre] = (int(M["m10"] / M["m00"]), int(M["m01"] / M["m00"]))

            # Contornos para el dibujo
            mask_temp = np.zeros((self.h_tablero, self.w_tablero), dtype=np.uint8)
            cv2.fillPoly(mask_temp, [pts_np], 255)
            contours, _ = cv2.findContours(mask_temp, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
            self.contours_objetos[nombre] = contours

        try:
            procesar('midfield', self.geo_data.get('centro', {}).get('midfield'))
            procesar('court_1', self.geo_data.get('izquierda', {}).get('court'))
            procesar('court_2', self.geo_data.get('derecha', {}).get('court'))
            areas_izq = self.geo_data.get('izquierda', {}).get('areas', [])
            if areas_izq: procesar('area_1', areas_izq[0])
            areas_der = self.geo_data.get('derecha', {}).get('areas', [])
            if areas_der: procesar('area_2', areas_der[0])
        except: pass

    def obtener_archivos(self):
        path = Path("/content/drive/MyDrive/Resultados")
        files = sorted(list(path.glob("**/anotaciones_completas/*.txt")))
        files.sort(key=lambda p: int(''.join(filter(str.isdigit, p.name))) if any(c.isdigit() for c in p.name) else 0)
        return files

    def leer_anotacion(self, path):
        detecciones = []
        try:
            with open(path, 'r') as f:
                for linea in f:
                    partes = linea.split()
                    if len(partes) < 3: continue
                    clase = partes[0]
                    if clase in self.centros_objetos:
                        coords = [float(x) for x in partes[2:]]
                        cx = sum(coords[0::2])/len(coords[0::2])
                        detecciones.append({'clase': clase, 'x': cx})
        except: pass
        return detecciones

