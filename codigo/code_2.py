class GestorDatos:
    def __init__(self):
        self.cargar_recursos()

    def cargar_recursos(self):
        self.path_tablero = "/content/drive/MyDrive/Recursos_Necesarios/Tablero.jpg"
        self.path_json = "/content/drive/MyDrive/Recursos_Necesarios/estructura_dual.json"
        if not os.path.exists(self.path_tablero): raise Exception("Falta Tablero")
        self.tablero = cv2.imread(self.path_tablero)
        self.h_tablero, self.w_tablero = self.tablero.shape[:2]
        with open(self.path_json, 'r') as f: self.geo_data = json.load(f)
        self.centros_objetos = {}
        self.limites_areas_mapa = {}
        self.mapa_court_top = 0
        self.mapa_court_bottom = self.h_tablero
        self.precalcular_objetos()

    def precalcular_objetos(self):
        def procesar(nombre, datos):
            if not datos: return
            pts_norm = datos.get('puntos_normalizados', [])
            if not pts_norm: return
            if 'area' in nombre:
                xs, ys = pts_norm[0::2], pts_norm[1::2]
                self.limites_areas_mapa[nombre] = (
                    int(min(xs)*self.w_tablero), int(max(xs)*self.w_tablero),
                    int(min(ys)*self.h_tablero), int(max(ys)*self.h_tablero)
                )
            pts_pix = []
            min_y, max_y = float('inf'), float('-inf')
            for i in range(0, len(pts_norm), 2):
                x = int(pts_norm[i]*self.w_tablero)
                y = int(pts_norm[i+1]*self.h_tablero)
                pts_pix.append([x, y])
                if y < min_y: min_y = y
                if y > max_y: max_y = y
            pts_np = np.array(pts_pix, dtype=np.int32)
            M = cv2.moments(pts_np)
            if M["m00"] != 0:
                self.centros_objetos[nombre] = (int(M["m10"]/M["m00"]), int(M["m01"]/M["m00"]))
            if 'court' in nombre:
                if min_y < self.mapa_court_top or self.mapa_court_top == 0: self.mapa_court_top = min_y
                if max_y > self.mapa_court_bottom or self.mapa_court_bottom == self.h_tablero: self.mapa_court_bottom = max_y

        try:
            procesar('midfield', self.geo_data.get('centro', {}).get('midfield'))
            procesar('court_1', self.geo_data.get('izquierda', {}).get('court'))
            procesar('court_2', self.geo_data.get('derecha', {}).get('court'))
            areas_izq = self.geo_data.get('izquierda', {}).get('areas', [])
            if areas_izq: procesar('area_1', areas_izq[0])
            areas_der = self.geo_data.get('derecha', {}).get('areas', [])
            if areas_der: procesar('area_2', areas_der[0])
            if self.mapa_court_top == 0: self.mapa_court_top = 20
            if self.mapa_court_bottom == self.h_tablero: self.mapa_court_bottom = self.h_tablero - 20
        except: pass

    def obtener_archivos(self):
        path = Path("/content/drive/MyDrive/Resultados")
        files = sorted(list(path.glob("**/anotaciones_completas/*.txt")))
        files.sort(key=lambda p: int(''.join(filter(str.isdigit, p.name))) if any(c.isdigit() for c in p.name) else 0)
        return files

    def leer_anotacion(self, path):
        detecciones = []
        try:
            with open(path, 'r') as f:
                for linea in f:
                    partes = linea.split()
                    if len(partes) < 3: continue
                    clase = partes[0]
                    coords_raw = [float(x) for x in partes[2:]]
                    puntos = []
                    for i in range(0, len(coords_raw), 2):
                        puntos.append([coords_raw[i], coords_raw[i+1]])
                    puntos_np = np.array(puntos, dtype=np.float32)
                    cx = np.mean(puntos_np[:, 0])
                    cy = np.mean(puntos_np[:, 1])
                    feet_y = np.max(puntos_np[:, 1])
                    detecciones.append({'clase': clase, 'x': cx, 'pies_norm': (cx, feet_y), 'poly_points': puntos_np})
        except: pass
        return detecciones

