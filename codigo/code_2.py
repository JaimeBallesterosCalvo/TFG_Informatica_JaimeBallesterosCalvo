class VerificadorSegmentacion:
    def __init__(self):
        self.tablero = None
        self.anotaciones = None
        self.classes = {
            0: 'area',
            1: 'corner',
            2: 'court',
            3: 'goal',
            4: 'midfield'
        }

    def cargar_tablero(self, ruta_tablero):
        """Carga y verifica la imagen del tablero"""
        print(f"📁 CARGANDO TABLERO: {ruta_tablero}")

        if not os.path.exists(ruta_tablero):
            raise FileNotFoundError(f"❌ No se encuentra el archivo: {ruta_tablero}")

        self.tablero = cv2.imread(ruta_tablero)
        if self.tablero is None:
            raise Exception("❌ No se pudo cargar la imagen del tablero")

        print(f"✅ Tablero cargado correctamente")
        print(f"   • Dimensiones: {self.tablero.shape[1]}x{self.tablero.shape[0]}")
        print(f"   • Canales: {self.tablero.shape[2]}")
        print(f"   • Tipo: {self.tablero.dtype}")

        return self.tablero

    def cargar_anotaciones_segmentacion(self, ruta_anotaciones):
        """Carga anotaciones en formato YOLO Instance Segmentation"""
        print(f"\n📝 CARGANDO ANOTACIONES DE SEGMENTACIÓN: {ruta_anotaciones}")

        if not os.path.exists(ruta_anotaciones):
            raise FileNotFoundError(f"❌ No se encuentra el archivo: {ruta_anotaciones}")

        with open(ruta_anotaciones, 'r', encoding='utf-8') as f:
            contenido = f.read().strip()

        lineas = contenido.split('\n')
        anotaciones = []

        print(f"   • Analizando {len(lineas)} líneas...")

        for i, linea in enumerate(lineas):
            linea = linea.strip()
            if not linea:
                continue

            partes = linea.split()

            # En instance segmentation, debe tener al menos 1 (clase) + 2 puntos (x,y)
            if len(partes) < 3:
                print(f"   ⚠️  Línea {i+1}: Muy pocos valores para segmentación: {len(partes)}")
                continue

            try:
                clase_id = int(partes[0])
                puntos_normalizados = [float(x) for x in partes[1:]]

                # Verificar que haya número par de coordenadas
                if len(puntos_normalizados) % 2 != 0:
                    print(f"   ⚠️  Línea {i+1}: Número impar de coordenadas: {len(puntos_normalizados)}")
                    continue

                anotaciones.append({
                    'tipo': 'segmentacion',
                    'clase_id': clase_id,
                    'clase_nombre': self.classes.get(clase_id, f"clase_{clase_id}"),
                    'puntos_normalizados': puntos_normalizados,
                    'num_puntos': len(puntos_normalizados) // 2,
                    'linea_original': f"{clase_id} [polígono con {len(puntos_normalizados)//2} puntos]"
                })

                print(f"   ✅ Línea {i+1}: {self.classes.get(clase_id, f'clase_{clase_id}')} - {len(puntos_normalizados)//2} puntos")

            except ValueError as e:
                print(f"   ❌ Error en línea {i+1}: {e}")

        return anotaciones

    def _mostrar_info_anotaciones(self):
        """Muestra información detallada de las anotaciones"""
        print(f"\n📊 INFORMACIÓN DE ANOTACIONES:")
        print(f"   • Total de anotaciones: {len(self.anotaciones)}")

        # Contar por clase
        conteo_clases = {}
        puntos_por_clase = {}

        for ann in self.anotaciones:
            clase_nombre = ann['clase_nombre']
            conteo_clases[clase_nombre] = conteo_clases.get(clase_nombre, 0) + 1
            puntos_por_clase[clase_nombre] = puntos_por_clase.get(clase_nombre, 0) + ann['num_puntos']

        print(f"   • Distribución por clase:")
        for clase, count in conteo_clases.items():
            avg_puntos = puntos_por_clase[clase] / count
            print(f"      - {clase}: {count} instancias, {puntos_por_clase[clase]} puntos total, {avg_puntos:.1f} pts/instancia")

    def mostrar_tablero(self):
        """Muestra la imagen del tablero"""
        if self.tablero is None:
            print("❌ No hay tablero cargado")
            return

        plt.figure(figsize=(12, 8))
        plt.imshow(cv2.cvtColor(self.tablero, cv2.COLOR_BGR2RGB))
        plt.title("TABLERO CARGADO - VERIFICACIÓN")
        plt.axis('off')
        plt.tight_layout()
        plt.show()

    def visualizar_segmentacion(self):
        """Visualiza las máscaras de segmentación"""
        if self.tablero is None:
            print("❌ Primero carga el tablero")
            return
        if not self.anotaciones:
            print("❌ No hay anotaciones cargadas")
            return

        print("\n🎨 VISUALIZANDO SEGMENTACIÓN...")
        img_visual = self.tablero.copy()
        h, w = img_visual.shape[:2]

        # Colores para diferentes clases - actualizados para 5 clases
        colores = {
            0: (255, 165, 0, 100),    # Naranja - area
            1: (255, 0, 255, 100),    # Magenta - corner
            2: (0, 255, 255, 100),    # Amarillo - court
            3: (0, 0, 255, 100),      # Rojo - goal
            4: (255, 255, 0, 100)     # Cian - midfield
        }

        # Crear una imagen transparente para las máscaras
        overlay = img_visual.copy()

        for i, ann in enumerate(self.anotaciones):
            if ann['tipo'] == 'segmentacion':
                clase_id = ann['clase_id']
                puntos_norm = ann['puntos_normalizados']

                # Convertir puntos normalizados a píxeles
                puntos_pixeles = []
                for j in range(0, len(puntos_norm), 2):
                    x = int(puntos_norm[j] * w)
                    y = int(puntos_norm[j+1] * h)
                    puntos_pixeles.append([x, y])

                # Convertir a array de numpy
                pts = np.array(puntos_pixeles, np.int32)

                # Dibujar polígono relleno semi-transparente
                color = colores.get(clase_id, (128, 128, 128, 100))
                cv2.fillPoly(overlay, [pts], color[:3])

                # Dibujar contorno
                cv2.polylines(img_visual, [pts], True, color[:3], 2)

                # Dibujar etiqueta
                if len(puntos_pixeles) > 0:
                    x_centro = sum(p[0] for p in puntos_pixeles) // len(puntos_pixeles)
                    y_centro = sum(p[1] for p in puntos_pixeles) // len(puntos_pixeles)
                    etiqueta = f"{ann['clase_nombre']}"
                    cv2.putText(img_visual, etiqueta, (x_centro-20, y_centro),
                               cv2.FONT_HERSHEY_SIMPLEX, 0.4, color[:3], 1)

        # Mezclar la imagen original con el overlay
        alpha = 0.4
        img_final = cv2.addWeighted(overlay, alpha, img_visual, 1 - alpha, 0)

        # Mostrar imagen
        plt.figure(figsize=(15, 10))
        plt.imshow(cv2.cvtColor(img_final, cv2.COLOR_BGR2RGB))
        plt.title("SEGMENTACIÓN - POLÍGONOS Y MÁSCARAS")
        plt.axis('off')
        plt.tight_layout()
        plt.show()

        return img_final

    def verificar_coordenadas_segmentacion(self):
        """Verifica que las coordenadas de segmentación sean válidas"""
        print("\n🔍 VERIFICANDO COORDENADAS DE SEGMENTACIÓN...")

        h, w = self.tablero.shape[:2]
        problemas = 0

        for i, ann in enumerate(self.anotaciones):
            if ann['tipo'] == 'segmentacion':
                puntos = ann['puntos_normalizados']

                # Verificar que todas las coordenadas estén en [0,1]
                if any(c < 0 or c > 1 for c in puntos):
                    print(f"   ⚠️  Anotación {i}: Coordenadas fuera de rango [0,1]")
                    problemas += 1

                # Verificar que al menos algunos puntos estén dentro de la imagen
                puntos_dentro = 0
                for j in range(0, len(puntos), 2):
                    x = puntos[j] * w
                    y = puntos[j+1] * h
                    if 0 <= x <= w and 0 <= y <= h:
                        puntos_dentro += 1

                if puntos_dentro == 0:
                    print(f"   ⚠️  Anotación {i}: Ningún punto dentro de la imagen")
                    problemas += 1

        if problemas == 0:
            print("   ✅ Todas las coordenadas de segmentación son válidas")
        else:
            print(f"   ⚠️  Se encontraron {problemas} problemas con las coordenadas")

    def analizar_contenido_anotaciones(self):
        """Analiza qué clases están presentes en las anotaciones"""
        print("\n🔎 ANALIZANDO CONTENIDO DE ANOTACIONES...")

        clases_presentes = set()
        for ann in self.anotaciones:
            clases_presentes.add(ann['clase_id'])

        print(f"   • Clases detectadas en el archivo: {sorted(clases_presentes)}")
        print(f"   • Clases utilizadas:")
        for clase_id in sorted(clases_presentes):
            count = sum(1 for ann in self.anotaciones if ann['clase_id'] == clase_id)
            puntos_totales = sum(ann['num_puntos'] for ann in self.anotaciones if ann['clase_id'] == clase_id)
            print(f"      - {self.classes[clase_id]} ({clase_id}): {count} instancias, {puntos_totales} puntos")

    def mostrar_leyenda(self):
        """Muestra una leyenda con los colores de cada clase"""
        print("\n🎨 LEYENDA DE COLORES:")
        colores_leyenda = {
            0: (255, 165, 0),    # Naranja - area
            1: (255, 0, 255),    # Magenta - corner
            2: (0, 255, 255),    # Amarillo - court
            3: (0, 0, 255),      # Rojo - goal
            4: (255, 255, 0)     # Cian - midfield
        }

        for clase_id, clase_nombre in self.classes.items():
            color = colores_leyenda.get(clase_id, (128, 128, 128))
            print(f"   • {clase_nombre} ({clase_id}): ███ Color {color}")

    def ejecutar_verificacion_completa(self, ruta_tablero, ruta_anotaciones):
        """Ejecuta la verificación completa"""
        print("🚀 INICIANDO VERIFICACIÓN COMPLETA - INSTANCE SEGMENTATION")
        print("=" * 60)

        try:
            # Cargar datos
            self.cargar_tablero(ruta_tablero)
            self.anotaciones = self.cargar_anotaciones_segmentacion(ruta_anotaciones)

            # Verificaciones
            self._mostrar_info_anotaciones()
            self.analizar_contenido_anotaciones()
            self.verificar_coordenadas_segmentacion()
            self.mostrar_leyenda()

            # Mostrar visualizaciones
            self.mostrar_tablero()
            self.visualizar_segmentacion()

            print("\n🎉 VERIFICACIÓN COMPLETADA EXITOSAMENTE!")
            return True

        except Exception as e:
            print(f"❌ Error durante la verificación: {e}")
            import traceback
            traceback.print_exc()
            return False

