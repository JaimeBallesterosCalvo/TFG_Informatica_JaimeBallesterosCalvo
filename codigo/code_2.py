class AnalizadorTactico:
    def __init__(self, gestor, config):
        self.g = gestor
        self.cx, self.cy, self.fov_deg = config
        self.fov_rad = math.radians(self.fov_deg)
        self.tan_fov_half = math.tan(self.fov_rad / 2)

        self.roster = []
        self._inicializar_roster()

    def _inicializar_roster(self):
        plantilla = [
            ('goalkeeper-team1', 1), ('player-team1', 4),
            ('goalkeeper-team2', 1), ('player-team2', 4)
        ]
        id_glob = 0
        for clase, cant in plantilla:
            for _ in range(cant):
                self.roster.append({'id': id_glob, 'clase': clase, 'px':0, 'py':0, 'estado':'VACIO', 'origen':''})
                id_glob += 1

    def calcular_angulo(self, detecciones):
        angulos, pesos = [], []
        for det in detecciones:
            if det['clase'] not in self.g.centros_objetos: continue
            obj_x, obj_y = self.g.centros_objetos[det['clase']]
            dx = obj_x - self.cx
            dy = obj_y - self.cy
            angulo_obj_mapa = math.atan2(dy, dx)
            offset_screen = det['x'] - 0.5
            angulo_correccion = math.atan(2 * offset_screen * self.tan_fov_half)
            angulo_cam = angulo_obj_mapa - angulo_correccion
            p = 1.0 - abs(offset_screen)
            if 'area' in det['clase']: p *= 4.0
            else: p *= 1.0
            angulos.append(angulo_cam)
            pesos.append(p)
        if not angulos: return None
        sin_sum = sum(w * math.sin(a) for w, a in zip(pesos, angulos))
        cos_sum = sum(w * math.cos(a) for w, a in zip(pesos, angulos))
        return math.atan2(sin_sum, cos_sum)

    def proyectar_punto_exacto(self, det, angulo_cam, target_w, target_h):
        x_norm, y_norm = det['pies_norm']
        y_top, y_bot = 0.3, 0.9
        ratio = (y_norm - y_top) / max(0.01, y_bot - y_top)
        mapa_court_range = self.g.mapa_court_bottom - self.g.mapa_court_top
        y_mapa = self.g.mapa_court_top + (ratio * mapa_court_range)

        radio = abs(self.cy - y_mapa)
        offset_screen = x_norm - 0.5
        angulo_rayo = angulo_cam + math.atan(2 * offset_screen * self.tan_fov_half)

        px = int(self.cx + radio * math.cos(angulo_rayo))
        py = int(self.cy + radio * math.sin(angulo_rayo))

        margen = 50
        if -margen <= px <= self.g.w_tablero + margen and -margen <= py <= self.g.h_tablero + margen:
            return px, py
        return None, None

    def es_visible_en_frame_0(self, px, py, angulo_f0):
        dx = px - self.cx
        dy = py - self.cy
        angulo_punto = math.atan2(dy, dx)
        diff = angulo_punto - angulo_f0
        while diff <= -math.pi: diff += 2*math.pi
        while diff > math.pi: diff -= 2*math.pi
        umbral_visibilidad = (math.radians(self.fov_deg) / 2) * 0.9
        if abs(diff) < umbral_visibilidad:
            return True
        return False

    def ejecutar_analisis(self, archivos, dets_f0, frame_shape):
        h_vid, w_vid = frame_shape[:2]
        target_h = self.g.h_tablero
        target_w = int(target_h * (w_vid / h_vid))

        # 1. FRAME 0 (REALIDAD)
        print("1️⃣ CAPA 1: Realidad Frame 0...")
        ang_f0 = self.calcular_angulo(dets_f0)
        if ang_f0 is None: ang_f0 = -1.57

        for det in dets_f0:
            if 'player' in det['clase'] or 'goalkeeper' in det['clase']:
                px, py = self.proyectar_punto_exacto(det, ang_f0, target_w, target_h)
                if px is None: continue
                for slot in self.roster:
                    if slot['clase'] == det['clase'] and slot['estado'] == 'VACIO':
                        slot['px'], slot['py'] = px, py
                        slot['estado'] = 'CONFIRMADO_REAL'
                        slot['origen'] = 'F0' # Marca de origen
                        break

        # 2. LOGICA PORTEROS
        print("2️⃣ CAPA 2: Lógica Porteros...")
        pos_gk1 = (80, self.g.h_tablero // 2)
        pos_gk2 = (self.g.w_tablero - 80, self.g.h_tablero // 2)
        for slot in self.roster:
            if slot['estado'] == 'VACIO' and 'goalkeeper' in slot['clase']:
                slot['px'], slot['py'] = pos_gk1 if 'team1' in slot['clase'] else pos_gk2
                slot['estado'] = 'INFERIDO_LOGICA'
                slot['origen'] = 'LOGIC'
                print(f"   -> GK inferido en meta.")

        # 3. BARRIDO FUTURO
        print("3️⃣ CAPA 3: Barrido Futuro (Con Filtro)...")
        candidatos = []

        for i, f in enumerate(archivos[1:151]):
            dets = self.g.leer_anotacion(f)
            if not dets: continue

            ang_actual = self.calcular_angulo(dets)
            if ang_actual is None: continue

            for det in dets:
                if 'player' not in det['clase']: continue
                px, py = self.proyectar_punto_exacto(det, ang_actual, target_w, target_h)
                if px is None: continue

                # ¿Conocido?
                conocido = False
                for slot in self.roster:
                    if slot['estado'] != 'VACIO':
                        if slot['clase'] == det['clase'] and math.hypot(px-slot['px'], py-slot['py']) < 70:
                            conocido = True; break
                if conocido: continue

                # ¿Candidato?
                match_cand = False
                for c in candidatos:
                    if c['clase'] == det['clase'] and math.hypot(px-c['px'], py-c['py']) < 70:
                        c['px'], c['py'] = int((c['px']+px)/2), int((c['py']+py)/2)
                        c['frames'] += 1
                        c['last'] = i
                        match_cand = True
                        if c['frames'] >= 3:
                            # --- FILTRO VISIBILIDAD ---
                            if self.es_visible_en_frame_0(c['px'], c['py'], ang_f0):
                                print(f"   ❌ RECHAZADO FANTASMA {c['clase']} en ({c['px']},{c['py']}) - Visible en F0")
                                c['frames'] = -999
                            else:
                                for slot in self.roster:
                                    if slot['clase'] == c['clase'] and slot['estado'] == 'VACIO':
                                        slot['px'], slot['py'] = c['px'], c['py']
                                        slot['estado'] = 'INFERIDO_FUTURO'
                                        slot['origen'] = f"F{i}" # <--- AQUÍ GUARDAMOS EL FRAME EXACTO
                                        print(f"   ✅ ACEPTADO {c['clase']} en ({c['px']},{c['py']}) - FRAME {i}")
                                        c['frames'] = -999
                                        break
                        break

                if not match_cand:
                    candidatos.append({'clase': det['clase'], 'px': px, 'py': py, 'frames': 1, 'last': i})

            candidatos = [c for c in candidatos if (i - c['last']) < 5 and c['frames'] != -999]

        return self.roster, ang_f0

