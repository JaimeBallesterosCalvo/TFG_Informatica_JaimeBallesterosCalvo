    def calcular_centroide_poligono(self, puntos):
        puntos_array = np.array(puntos, dtype=np.float32)
        M = cv2.moments(puntos_array)
        if M["m00"] != 0:
            centro_x = int(M["m10"] / M["m00"])
            centro_y = int(M["m01"] / M["m00"])
        else:
            x_coords = puntos_array[:, 0]
            y_coords = puntos_array[:, 1]
            centro_x = int(np.mean(x_coords))
            centro_y = int(np.mean(y_coords))
        return (centro_x, centro_y)

