    def precalcular_objetos(self):
        def procesar(nombre, datos):
            if not datos: return
            pts_norm = datos.get('puntos_normalizados', [])
            if not pts_norm: return
            pts_pix = []

            min_y = float('inf')
            max_y = float('-inf')

            for i in range(0, len(pts_norm), 2):
                x = int(pts_norm[i]*self.w_tablero)
                y = int(pts_norm[i+1]*self.h_tablero)
                pts_pix.append([x, y])
                if y < min_y: min_y = y
                if y > max_y: max_y = y

            pts_np = np.array(pts_pix, dtype=np.int32)
            M = cv2.moments(pts_np)
            if M["m00"] != 0:
                self.centros_objetos[nombre] = (int(M["m10"] / M["m00"]), int(M["m01"] / M["m00"]))

            mask_temp = np.zeros((self.h_tablero, self.w_tablero), dtype=np.uint8)
            cv2.fillPoly(mask_temp, [pts_np], 255)
            contours, _ = cv2.findContours(mask_temp, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
            self.contours_objetos[nombre] = contours

            if 'court' in nombre:
                if min_y < self.mapa_court_top or self.mapa_court_top == 0:
                    self.mapa_court_top = min_y
                if max_y > self.mapa_court_bottom or self.mapa_court_bottom == self.h_tablero:
                    self.mapa_court_bottom = max_y

        try:
            procesar('midfield', self.geo_data.get('centro', {}).get('midfield'))
            procesar('court_1', self.geo_data.get('izquierda', {}).get('court'))
            procesar('court_2', self.geo_data.get('derecha', {}).get('court'))
            areas_izq = self.geo_data.get('izquierda', {}).get('areas', [])
            if areas_izq: procesar('area_1', areas_izq[0])
            areas_der = self.geo_data.get('derecha', {}).get('areas', [])
            if areas_der: procesar('area_2', areas_der[0])

            if self.mapa_court_top == 0: self.mapa_court_top = 20
            if self.mapa_court_bottom == self.h_tablero: self.mapa_court_bottom = self.h_tablero - 20
        except: pass

