    def convertir_puntos_normalizados(self, puntos_normalizados, ancho, alto):
        puntos_pixeles = []
        for i in range(0, len(puntos_normalizados), 2):
            x = int(puntos_normalizados[i] * ancho)
            y = int(puntos_normalizados[i + 1] * alto)
            puntos_pixeles.append([x, y])
        return np.array(puntos_pixeles, dtype=np.int32)

    def configurar_geometria_tablero(self):
        print("🗺️ CONFIGURANDO GEOMETRÍA DEL TABLERO...")

        self.elementos_tablero = {}

        # MIDFIELD
        midfield_tablero = self.estructura_campo['centro']['midfield']
        puntos_midfield = self.convertir_puntos_normalizados(
            midfield_tablero['puntos_normalizados'], self.ancho_tablero, self.alto_tablero)
        self.elementos_tablero['midfield'] = {
            'poligono': puntos_midfield,
            'centroide': self.calcular_centroide_poligono(puntos_midfield)
        }

        # COURTS
        for lado in ['izquierda', 'derecha']:
            court = self.estructura_campo[lado]['court']
            puntos_court = self.convertir_puntos_normalizados(
                court['puntos_normalizados'], self.ancho_tablero, self.alto_tablero)
            clave = 'court_1' if lado == 'izquierda' else 'court_2'
            self.elementos_tablero[clave] = {
                'poligono': puntos_court,
                'centroide': self.calcular_centroide_poligono(puntos_court),
                'lado': lado
            }

